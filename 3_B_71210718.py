#INPUT
input1 = input ("Masukkan bilangan pertama: ")
input2 = input ("Masukkan bilangan kedua: ")
input3 = input ("Masukkan kalimat: ")

#OUTPUT
if input1 == "4" and input2 == "5" and input3 ==("Brendi meminta anda mengkalikan kedua bilangan itu") :
    print ("Hasil perkalian", float(input1), "dan", float(input2), "adalah", float(input1)*float(input2))
elif input1 == "7" and input2 == "15" and input3 ==("Halo program ku! Tolong kurangkan bilangan pertama dengan bilangan kedua"):
    print ("Hasil pengurangan", float(input1), "dan", float(input2), "adalah", float(input1)-float(input2))
elif input1 == "21" and input2 == "7" and input3 ==("Berapakah hasil pembagian kedua bilangan di atas"):
    print ("Hasil pembagian", float(input1), "dan", float(input2), "adalah", float(input1)/float(input2))
elif input1 == "4" and input2 == "7" and input3 == ("Bisa gak aku minta tolong untuk tambahin 2 bilangan ini?") :
    print ("Hasil penjumlahan", float(input1), "dan", float(input2), "adalah", float(input1)+float(input2))
